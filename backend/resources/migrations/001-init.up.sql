CREATE TABLE IF NOT EXISTS ops.source_dataset (
    id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_key               TEXT NOT NULL UNIQUE,
    display_name             TEXT NOT NULL,
    authority                TEXT NOT NULL,
    source_url               TEXT NOT NULL,
    license_name             TEXT,
    license_url              TEXT,
    refresh_cadence          TEXT NOT NULL,
    current_version          TEXT,
    current_published_at     TIMESTAMPTZ,
    last_successful_run_at   TIMESTAMPTZ,
    expected_record_count    INTEGER,
    expected_region_count    INTEGER,
    membership_review_status TEXT NOT NULL DEFAULT 'not_applicable',
    metadata                 JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at               TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at               TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT source_membership_review_status
      CHECK (membership_review_status IN ('not_applicable', 'approved', 'review_required'))
);
--;;

CREATE TABLE IF NOT EXISTS ops.ingestion_run (
    id                    UUID PRIMARY KEY,
    source_dataset_id     BIGINT NOT NULL REFERENCES ops.source_dataset(id),
    status                TEXT NOT NULL,
    upstream_version      TEXT,
    upstream_published_at TIMESTAMPTZ,
    started_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
    finished_at           TIMESTAMPTZ,
    fetched_count         INTEGER,
    staged_count          INTEGER,
    activated_count       INTEGER,
    rejected_count        INTEGER NOT NULL DEFAULT 0,
    checksum_sha256       TEXT,
    error_class           TEXT,
    error_message         TEXT,
    validation_report     JSONB NOT NULL DEFAULT '{}'::jsonb,
    CONSTRAINT ingestion_status
      CHECK (status IN ('running', 'succeeded', 'failed', 'skipped_locked'))
);
--;;

CREATE INDEX IF NOT EXISTS ingestion_run_source_started_idx
    ON ops.ingestion_run (source_dataset_id, started_at DESC);
--;;

CREATE TABLE IF NOT EXISTS ops.ingestion_staging (
    ingestion_run_id UUID NOT NULL REFERENCES ops.ingestion_run(id) ON DELETE CASCADE,
    record_type      TEXT NOT NULL,
    upstream_id      TEXT NOT NULL,
    normalized_record JSONB NOT NULL,
    staged_at        TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (ingestion_run_id, record_type, upstream_id)
);
--;;

CREATE TABLE IF NOT EXISTS ops.ingestion_rejection (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ingestion_run_id UUID NOT NULL REFERENCES ops.ingestion_run(id) ON DELETE CASCADE,
