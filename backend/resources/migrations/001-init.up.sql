CREATE OR REPLACE VIEW analytics.volcanoes AS
SELECT v.volcano_number,
       v.name,
       v.slug,
       v.country,
       v.subregion,
       r.name AS region,
       v.primary_volcano_type AS volcano_type,
       v.tectonic_setting,
       v.elevation_m,
       v.last_eruption_year,
       ST_Y(v.geom)::double precision AS latitude,
       ST_X(v.geom)::double precision AS longitude,
       rm.included AS in_smithsonian_prof,
       v.source_version
FROM core.volcano v
LEFT JOIN core.volcanic_region r ON r.id = v.volcanic_region_id
LEFT JOIN LATERAL (
  SELECT membership.included
  FROM core.ring_membership membership
  WHERE membership.volcano_number = v.volcano_number
    AND membership.definition_key = 'smithsonian-prof'
  ORDER BY membership.dataset_version DESC
  LIMIT 1
) rm ON true;
--;;

CREATE OR REPLACE VIEW analytics.eruptions AS
SELECT e.eruption_number,
       e.volcano_number,
       v.name AS volcano_name,
       v.country,
       r.name AS region,
       e.start_year,
       e.start_month,
       e.start_day,
       e.date_precision,
       e.vei,
       e.certainty,
       e.source_version
FROM core.eruption e
JOIN core.volcano v USING (volcano_number)
LEFT JOIN core.volcanic_region r ON r.id = v.volcanic_region_id;
--;;

CREATE OR REPLACE VIEW analytics.earthquakes AS
SELECT event_id,
       occurred_at,
       magnitude,
       magnitude_type,
       depth_km,
       place,
       significance,
       tsunami_flag,
       ST_Y(geom)::double precision AS latitude,
       ST_X(geom)::double precision AS longitude,
       source_version
FROM core.earthquake
WHERE NOT is_deleted;
--;;

